package com.miciaha.rendezvous.utilities.fields;

/**
 * The enum Field type describes different types of fields tracked in a form.
 *
 * FUTURE_ENHANCEMENT adding additional field types as necessary.
 */
public enum FieldType {
    /**
     * Text field type that should be interpreted as a String.
     */
    TEXT,
    /**
     * Integer field type.
     */
    INTEGER,
    /**
     * Double field type.
     */
    DOUBLE,
    PASSWORD
}
